public class Calculadora {
    /**
     * Este metodo é utilizado para somar dois numeros inteiros
     * @param numeroUm é o primeiro parametro do metodo
     * @param numeroDois é o segundo paramentro do metodo
     * @return o resultado deste metodo é a soma dos dois numeros
     * */
    public int somar(int numeroUm, int numeroDois){
        return numeroUm + numeroDois;
    }
}
